package com.datamation.megaheaters.model;

public class TERMS {

    private String ID;
    private String TERMCODE;
    private String TERMDES;
    private String TERMDISPER;

    public String getID() {
        return ID;
    }

    public void setID(String iD) {
        ID = iD;
    }

    public String getTERMCODE() {
        return TERMCODE;
    }

    public void setTERMCODE(String tERMCODE) {
        TERMCODE = tERMCODE;
    }

    public String getTERMDES() {
        return TERMDES;
    }

    public void setTERMDES(String tERMDES) {
        TERMDES = tERMDES;
    }

    public String getTERMDISPER() {
        return TERMDISPER;
    }

    public void setTERMDISPER(String tERMDISPER) {
        TERMDISPER = tERMDISPER;
    }


}
